CREATE TABLE cad_pessoa(
	
	id BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
	nome VARCHAR(100) NOT NULL,
	sobrenome VARCHAR(60) NOT NULL,
	nome_mae VARCHAR(150) NOT NULL,
	nome_pai VARCHAR(150) NOT NULL,
	cpf VARCHAR(15) NOT NULL,
	rg VARCHAR(15) NULL,
	pis VARCHAR(25) NULL,
	cnh VARCHAR(25) NULL,
	idade BIGINT(20) NOT NULL,
	data_nascimento DATE NOT NULL,
	estado_civil VARCHAR(20) NOT NULL,
	pessoa_sexo VARCHAR(20) NOT NULL,
	pessoa_tipo VARCHAR(50) NOT NULL,
	raca_cor VARCHAR(25) NOT NULL,
	nacionalidade VARCHAR(50) NOT NULL,
	pessoa_grau_parentesco VARCHAR(60) NOT NULL,
	logradouro VARCHAR(150) NULL,
	numero VARCHAR(10) NULL,
	complemento VARCHAR(40) NULL,
	bairro VARCHAR(100) NULL,
	cep VARCHAR(10) NULL,
	cidade VARCHAR(180) NULL,
	estado VARCHAR(2) NULL,
	orgao_emissor VARCHAR(12) NULL,
	estado_orgao_emissor_rg VARCHAR(2) NULL,
	data_emissao_rg DATE NULL,
	numero_titulo_eleitor VARCHAR(40) NULL,
	zona_titulo_eleitor VARCHAR(30) NULL,
	data_emissao_titulo_eleitor DATE NULL,
	ativo TINYINT(1) NOT NULL DEFAULT 0
	
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
